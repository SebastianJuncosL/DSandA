#include <iostream>
using namespace std;

// 1-------------------------------------------------------------------------

int multiply(int base, int times, int result)
{
    if (times == 0)
        return result;
    return multiply(base, times-=1, result += base);
}

// 2-------------------------------------------------------------------------

int square(int base, int times, int result)
{
    if (times == 0)
        return result;
    return square(base, times-=1, result *= base);
}

